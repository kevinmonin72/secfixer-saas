export interface WpTarget {
  siteUrl: string;
  username: string;
  appPassword: string;
}

async function wpFetch(target: WpTarget, path: string, opts: RequestInit = {}) {
  const base = target.siteUrl.replace(/\/$/, "");
  const credentials = btoa(`${target.username}:${target.appPassword}`);
  const res = await fetch(`${base}/wp-json${path}`, {
    ...opts,
    headers: {
      Authorization: `Basic ${credentials}`,
      "Content-Type": "application/json",
      ...(opts.headers || {}),
    },
  });
  if (!res.ok) {
    const err = await res.json().catch(() => ({}));
    throw new Error(`WordPress API ${res.status}: ${(err as { message?: string }).message || res.statusText}`);
  }
  return res.json();
}

export async function validateWp(target: WpTarget): Promise<{ name: string }> {
  return wpFetch(target, "/wp/v2/users/me") as Promise<{ name: string }>;
}

