export async function updateThemeOption(target: WpTarget, optionName: string, value: string): Promise<void> {
  await wpFetch(target, "/wp/v2/settings", {
    method: "POST",
    body: JSON.stringify({ [optionName]: value }),
  });
}

export async function getActiveTheme(target: WpTarget): Promise<string> {
  const data = await wpFetch(target, "/wp/v2/themes?status=active") as { stylesheet: string }[];
  return data[0]?.stylesheet || "unknown";
}

export function generateWpMuPlugin(securityHeaders: string[]): string {
  const lines = securityHeaders.map(h => {
    const [name, ...rest] = h.split(": ");
    return `  header('${name}: ${rest.join(": ")}');`;
  });
  return `<?php
/**
 * Plugin Name: SecFixer Security Headers
 * Description: Auto-generated by SecFixer SaaS — security headers hardening.
 * Version: 1.0
 */
defined('ABSPATH') || exit;

add_action('send_headers', function() {
${lines.join("\n")}
});
`;
}
